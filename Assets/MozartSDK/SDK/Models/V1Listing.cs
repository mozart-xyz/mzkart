namespace Mozart
{
using System;
using System.Collections;

public class V1Listing
{

	/*[string] The id of the Listing.*/
	public string id;

	/*[string] The amount of the FT that is required to purchase this NFT.*/
	public string ftAmount;

	/*[string] The id of the FT that is the currency being requested in exchange for the NFT.*/
	public string ftId;

	/*[string] The id of the Nft that is for sale.*/
	public string nftId;

	/*[string] The id of the User who is selling the NFT.*/
	public string sellerId;

	
}
}